<h3>Comment procéder sur ce site ?</h3>

<p class='flottant2'><img src="images/vote.jpg"/></p>

<p>Le principe est simple : rensignez dans l'onglet "identification" votre nom de famille.
Un <b>mail</b> sera alors automatiquement envoyé sur <b>votre boîte mail centralienne</b>. Ce mail contiendra un <b>mot de passe</b> 
contenant vos initiales et 8 caractères choisis aléatoirement.</p>
<p>Votez ou inscrivez-vous et ajoutez votre mot de passe ! (Mais attention :
sur ce site, nous vous demanderons d'envoyer votre mot de passe et votre vote sur la même page. Ne vous précipitez pas après avoir
entré votre mot de passe !)</p>

